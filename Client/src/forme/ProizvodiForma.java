    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int row = tblProizvodi.getSelectedRow();

        if (row >= 0) {
            ModelTabeleProizvod mt = (ModelTabeleProizvod) tblProizvodi.getModel();
            Proizvod p = mt.vratiProizvod(row);

            IzmeniProizvod izp = new IzmeniProizvod(this, true, p);
            izp.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Mora biti selektovan red u tabeli!");
            return;
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

