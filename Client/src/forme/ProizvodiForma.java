    private void prepareForm() throws Exception {
        popuniTabeluProizvodi();
    }

    private void popuniTabeluProizvodi() throws Exception {
        ModelTabeleProizvod mtp = (ModelTabeleProizvod) tblProizvodi.getModel();
        List<Proizvod> proizvodi = Kontroler.getInstance().vratiProizvode();
        mtp.setLista(proizvodi);
    }

    void izmeniProizvod(Proizvod p) throws Exception {
        ModelTabeleProizvod mt = (ModelTabeleProizvod) tblProizvodi.getModel();
        mt.izmeniProizvod(tblProizvodi.getSelectedRow(), p);

        Kontroler.getInstance().izmeniProizvod(p);
    }

