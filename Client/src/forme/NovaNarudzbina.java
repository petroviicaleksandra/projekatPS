    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selectedRow = tblStavke.getSelectedRow();

        if (selectedRow >= 0) {
            ModelTabeleStavkeNaruzbine mt = (ModelTabeleStavkeNaruzbine) tblStavke.getModel();
            mt.obrisiStavku(selectedRow);
        }
        total = izracunajTotal();
        txtIznos.setText(String.valueOf(total));
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            if (txtIznos.getText().equals("") || txtIznos.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
                return;
            }
            int total = Integer.parseInt(txtIznos.getText());
            Klijent k = klijent;

            if (txtKlijenti.getText().isEmpty() || txtKlijenti.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Klijent mora biti izabran!");
                return;
            }

            p = Kontroler.getInstance().getCurrentProdavac();
            ModelTabeleStavkeNaruzbine mt = (ModelTabeleStavkeNaruzbine) tblStavke.getModel();
            if (mt.getLista().size() < mt.getRowCount()) {
                JOptionPane.showMessageDialog(this, "Narudzbina mora imati barem jednu stavku!");
                return;
            }
            Narudzbina n = new Narudzbina(-1, total, new Timestamp(System.currentTimeMillis()), p, klijent);
            n.setListaStavkiNarudzbine(mt.getLista());
            Kontroler.getInstance().dodajNarudzbinu(n);
            
            JOptionPane.showMessageDialog(this, "Uspesno dodavanje!");

            NarudzbineForma nf = new NarudzbineForma();
            nf.setVisible(true);
            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(KlijentskaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

