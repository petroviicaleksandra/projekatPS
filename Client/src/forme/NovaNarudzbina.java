    void izaberiKlijenta(Klijent k) {
        txtKlijenti.setText(k.getIme() + " " + k.getPrezime());
        this.klijent = k;
    }

    void dodajStavku(StavkaNarudzbine sn) {
        ModelTabeleStavkeNaruzbine mt = (ModelTabeleStavkeNaruzbine) tblStavke.getModel();
        mt.dodajStavku(sn);

        total = izracunajTotal();
        txtIznos.setText(String.valueOf(total));
    }

    private int izracunajTotal() {
        ModelTabeleStavkeNaruzbine mt = (ModelTabeleStavkeNaruzbine) tblStavke.getModel();
        int total = 0;
        int iznos;
        for (int i = 0; i < mt.getRowCount(); i++) {
            iznos = (int) mt.getValueAt(i, 3);
            total += iznos;
        }
        return total;
    }
