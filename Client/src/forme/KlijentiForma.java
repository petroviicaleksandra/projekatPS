    private void prepareForm() throws Exception {
        popuniTabeluKlijenti();
    }

    private void popuniTabeluKlijenti() throws Exception {
        ModelTabeleKlijent mtk = (ModelTabeleKlijent) tblKlijenti.getModel();
        List<Klijent> klijenti = Kontroler.getInstance().vratiKlijente();
        mtk.setLista(klijenti);
    }

    void izmeniKlijenta(Klijent k) throws Exception {
        ModelTabeleKlijent mt = (ModelTabeleKlijent) tblKlijenti.getModel();
        mt.izmeniKlijenta(tblKlijenti.getSelectedRow(), k);

        Kontroler.getInstance().izmeniKlijenta(k);
    }

