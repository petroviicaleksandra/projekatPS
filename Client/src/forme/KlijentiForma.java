    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int row = tblKlijenti.getSelectedRow();

        if (row >= 0) {
            ModelTabeleKlijent mt = (ModelTabeleKlijent) tblKlijenti.getModel();
            Klijent k = mt.vratiKlijente(row);

            IzmeniKlijenta izk = new IzmeniKlijenta(this, true, k);
            izk.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Mora biti selektovan red u tabeli!");
            return;
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

